#include "libft.h"
int toupper(int c)
{   
	if( c >= 'a' && c <= 'z')
	{
		c = c - 32;
	    return (c);
	}
	else
		return (c);
}
int main()
{
	int a;
	a= toupper(77);
	printf("%c",a);
}
