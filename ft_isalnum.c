#include "libft.h"
int ft_isalnum (int n)
{
	if ( (n >= 'a' && n <= 'z') || (n >= 'A' && n <= 'Z')
			|| ( n >='0' && n <='9'))
		return (1);
	else 
		return (0);
}


int main()
{
	int a;
	a = ft_isalnum(57);
	printf("%d", a);
}
